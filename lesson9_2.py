# Читаем количество чисел в первом списке (необязательно, если не указано)
# n = int(input())

# Читаем первый список чисел
list1 = list(map(int, input().split()))

# Читаем количество чисел во втором списке (необязательно, если не указано)
# m = int(input())

# Читаем второй список чисел
list2 = list(map(int, input().split()))

# Преобразуем списки в множества для быстрого пересечения
set1 = set(list1)
set2 = set(list2)

# Находим пересечение множеств
common = set1.intersection(set2)

# Выводим количество общих чисел
print(len(common))